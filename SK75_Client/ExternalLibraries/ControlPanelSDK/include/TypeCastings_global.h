#define toUInt16(number)    static_cast<uint16_t>(number)
#define toInt32(number)     static_cast<int32_t>(number)
#define toUInt32(number)    static_cast<uint32_t>(number)
#define toFLT(number)       static_cast<float>(number)
#define toDBL(number)       static_cast<double>(number)
